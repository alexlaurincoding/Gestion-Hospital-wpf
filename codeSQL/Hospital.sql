CREATE TABLE PARENT (
    ref_parent INT CONSTRAINT PK_PARENT PRIMARY KEY,
    nom_PARENT CHAR(100) NOT NULL,
    PRENOM_PARENT CHAR(100) NOT NULL,
    ADRESSE_PARENT CHAR(100) NOT NULL,
    VILLE_PARENT CHAR(100) NOT NULL,
    PROVINCE_PARENT CHAR(100) NOT NULL,
    CODE_POSTAL_PARENT CHAR(10) NOT NULL,
    TELEPHONE_PARENT INT NOT NULL
);

CREATE TABLE TYPE_LIT (
    NUMERO_TYPE INT CONSTRAINT PK_TYPE_LIT PRIMARY KEY,
    DESCRIPTION_LIT CHAR(100) NOT NULL
);

CREATE TABLE DEPARTEMENT (
    ID_DEPARTEMENT INT CONSTRAINT PK_ID_DEPARTEMENT PRIMARY KEY,
    NOM_DEPARTEMENT CHAR(100) NOT NULL,
);

CREATE TABLE MEDECIN (
    ID_MEDECIN INT CONSTRAINT PK_ID_MEDECIN PRIMARY KEY,
    NOM_MEDECIN CHAR(100) NOT NULL,
    PRENOM_MEDECIN CHAR(100) NOT NULL,
    SPECIALITE CHAR(100) NOT NULL
);

CREATE TABLE COMPAGNIE_ASSURANCE (
    ID_ASSURANCE INT CONSTRAINT PK_ID_ASSURANCE PRIMARY KEY,
    NOM_COMPAGNIE CHAR(100) NOT NULL
);

CREATE TABLE PATIENT (
    NAM CHAR(12) CONSTRAINT PK_PATIENT PRIMARY KEY,
    DATE_NAISSANCE_PATIENT DATE NOT NULL,
    NOM_PATIENT CHAR(100) NOT NULL,
    PRENOM_PATIENT CHAR(100) NOT NULL,
    ADRESSE_PATIENT CHAR(100) NOT NULL,
    VILLE_PATIENT CHAR(100) NOT NULL,
    PROVINCE_PATIENT CHAR(100) NOT NULL,
    CODE_POSTAL_PATIENT CHAR(10) NOT NULL,
    TELEPHONE_PATIENT INT NOT NULL,
    ID_ASSURANCE INT CONSTRAINT FK_COMPAGNIE_ASSURANCE 
		REFERENCES COMPAGNIE_ASSURANCE (ID_ASSURANCE),
    REF_PARENT INT CONSTRAINT FK_REF_PARENT
		REFERENCES PARENT (REF_PARENT)
);

CREATE TABLE LIT (
    NUMERO_LIT INT CONSTRAINT PK_NUMERO_LIT PRIMARY KEY,
    LIT_OCCUPE BIT NOT NULL,
    NUMERO_TYPE INT CONSTRAINT FK_NUMERO_TYPE
        REFERENCES TYPE_LIT (NUMERO_TYPE),
    ID_DEPARTEMENT INT CONSTRAINT FK_ID_DEPARTEMENT
        REFERENCES DEPARTEMENT (ID_DEPARTEMENT)
);

CREATE TABLE DOSSIER_ADMISSION (
    ID_ADMISSION INT CONSTRAINT PK_ID_ADMISSION PRIMARY KEY,
    CHIRURGIE_PROGRAMME CHAR(100),
    DATE_ADMISSION DATE NOT NULL,
    DATE_CHIRURGIE DATE,
    DATE_CONGE DATE,
    NAM CHAR(12) CONSTRAINT FK_NAM
		REFERENCES PATIENT (NAM),
    NUMERO_LIT INT CONSTRAINT FK_NUMERO_LIT
		REFERENCES LIT (NUMERO_LIT),
    ID_MEDECIN INT CONSTRAINT FK_ID_MEDECIN 
        REFERENCES MEDECIN (ID_MEDECIN)
);


